raw_disp() {
  ascii=$(printf "%d" "'$2")
  value=${data[$ascii]}
  i2cset -y 1 $1 $value
}

disp() {
  if [ "$1" = "" ]; then
    raw_disp 0x34 ''
    return
  fi
  i2cset -y 1 0x24 0x1
  raw_disp 0x34 ${1:0:1}
  raw_disp 0x35 ${1:1:1}
  raw_disp 0x36 ${1:2:1}
  raw_disp 0x37 ${1:3:1}
  sleep 0.3
  disp "${1:1}"
}

while `true`; do
  IFS=$'\n'
  export IP=`hostname -I 2>&1 | sed "s# .*##g"`
  export RAM=`awk '/^Mem/ { printf("%.0f", 100*$3/$2) }' <(free -m)`
  export CPU=`top -bn1 | grep "Cpu(s)" | awk '{printf("%.0f",100-$8)}'`
  for i in `envsubst < /tmp/screen`; do
    disp "$i"
    sleep 1
  done
done